    private void deleteSelected(java.awt.event.ActionEvent evt) {
        var selected = itemTable.getSelectedRows();
        var selectedItems = Arrays.stream(selected).mapToObj((i) -> itemTableModel.getRowItem(i)).toList();
        int selection = JOptionPane.showConfirmDialog(this, "Delete items with " + String.join(" ,", Arrays.stream(selected).mapToObj(String::valueOf).toArray(String[]::new)) + " IDs?", "Delete Selected Items",
                    JOptionPane.YES_NO_OPTION);
        
        if (selection == 0) {
            for (Item item : selectedItems) {
                itemTableModel.removeItem(item);
            }
        }

        itemTableModel.fireTableDataChanged();
    }
    
