    private void initTable() {
        itemArray.add(new Item(1, "item 1", 123.12f, 10));
        itemArray.add(new Item(2, "item 2", 123.12f, 10));
        itemArray.add(new Item(3, "item 3", 123.12f, 10));
        itemArray.add(new Item(4, "item 4", 123.12f, 10));
        itemArray.add(new Item(5, "item 5", 123.12f, 10));

        itemTableModel = new ItemTableModel(itemArray);

        itemTable.setModel(itemTableModel);
        itemTable.setShowHorizontalLines(true);
        itemTable.setAutoCreateRowSorter(true);
        itemTable.getTableHeader().setReorderingAllowed(false);

        ListSelectionModel cellSelectionModel = itemTable.getSelectionModel();

        cellSelectionModel.addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                int[] selectedRows = itemTable.getSelectedRows();

                rowActionButtonContainer.setVisible(selectedRows.length > 0);
                toolbarLabel.setText(selectedRows.length + " rows selected.");
            }

        });
    }

    private void initDialogs() {
        addItemDialog = new DialogWithInputs(this, "Add New Item", "Add New Item", inputData -> {
            var id = itemArray.size();

            itemArray.add(
                    new Item(id,
                            inputData.get("Name").get(0).getText(),
                            Float.parseFloat(inputData.get("Price").get(0).getText()),
                            Integer.parseInt(inputData.get("Quantity").get(0).getText())));

            JOptionPane.showMessageDialog(addItemDialog, "Added new item with ID: " + id, "Successfully added item",
                    JOptionPane.INFORMATION_MESSAGE);
            itemTableModel.fireTableDataChanged();
            addItemDialog.setVisible(false);
            addItemDialog.clearAll();
        });

        addItemDialog
                .addInput(new InputWithLabel("Name").setValidator("^[a-zA-Z0-9]*$",
                        "Name can only contain letters and numbers"))
                .addInput(new InputWithLabel("Price").setValidator("^-?\\d+(\\.\\d+)?$",
                        "Price has to be a number or a decimal"))
                .addInput(new InputWithLabel("Quantity").setValidator("^-?\\d+$", "Quantity has to be a whole number"));
    }

